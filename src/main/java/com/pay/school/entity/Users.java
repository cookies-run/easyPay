public class Users {
    private String user_mobile;
    private String user_name;
    private String user_relation;
    private String user_change_mobile;

    public String getUser_mobile() {
        return user_mobile;
    }

    public void setUser_mobile(String user_mobile) {
        this.user_mobile = user_mobile;
    }

    public String getUser_name() {
        return user_name;
    }

    public void setUser_name(String user_name) {
        this.user_name = user_name;
    }

    public String getUser_relation() {
        return user_relation;
    }

    public void setUser_relation(String user_relation) {
        this.user_relation = user_relation;
    }

    public String getUser_change_mobile() {
        return user_change_mobile;
    }

    public void setUser_change_mobile(String user_change_mobile) {
        this.user_change_mobile = user_change_mobile;
    }
}
